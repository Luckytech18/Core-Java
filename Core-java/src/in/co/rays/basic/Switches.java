package in.co.rays.basic;

public class Switches {
public static void main(String[]args ) {
	
	String d = "apple";
	
	switch (d) {
	case "grapes" :
		
		System.out.println("10 Rupees kg");
		break;
	
	case "kiwi":
	System.out.println("20 Rupees kg");
	
	
	break;
	
	case "apple" :
		
		System.out.println("30 Rupees kg");
		
	default:

		System.out.println("this is default");
		break;
		}
	
	}
}
