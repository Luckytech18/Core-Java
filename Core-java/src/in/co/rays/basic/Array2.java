package in.co.rays.basic;

public class Array2 {

	public static void main(String[] args) {
		int[] a = {7 , 29 , 18 , 45 , 17 , 33};
		
		System.out.println(a.length);
		
		int n = 18;
		int position = 0;
		
		
	
		
		
		//for loop
	for (int i =0; i < a.length; i++) {
		if (a[i] == n ) {
			position = i; 
			System.out.println(position);
		}
	}
	
	
		 
	 

		}
		
		
	}

		
